# Escreva um código usando o que você aprendeu sobre o operador módulo e verificações condicionais em Python
# para verificar se o número na área de entrada é par ou ímpar.
# Se for ímpar, imprima a palavra "ímpar", caso contrário, imprima "par".
numero = int(input("Digite um número: "))
if numero % 2 == 0:
    print("par")
else:
    print("ímpar")
